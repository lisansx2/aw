package cn.com.cslc.aw.settings.gamesetting.dto;


public class GameSettingDto {

	private String gameNo;
	
	private String gameName;
	

	private String gameSystemNo;


	public String getGameNo() {
		return gameNo;
	}

	public void setGameNo(String gameNo) {
		this.gameNo = gameNo;
	}

	public String getGameName() {
		return gameName;
	}

	public void setGameName(String gameName) {
		this.gameName = gameName;
	}

	public String getGameSystemNo() {
		return gameSystemNo;
	}

	public void setGameSystemNo(String gameSystemNo) {
		this.gameSystemNo = gameSystemNo;
	}


}
